//conditional statements
let num = 15;

if (num > 20) {
    console.log("Number is greater than 20");
} 
else if (num === 20) {
    console.log("Number is exactly 20");
} 
else {
    console.log("Number is less than 20");
}

// for loop
for (let i = 1; i <= 5; i++) {
    console.log("For Loop Count:", i);
}

//while loop
let j = 1;
while (j <= 5) {
    console.log("While Loop Count:", j);
    j++;
}

// do while loop
let k = 1;
do {
    console.log("Do-While Loop Count:", k);
    k++;
} while (k <= 5);